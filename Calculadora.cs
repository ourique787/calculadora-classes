class Calculadora{
    public double Soma(double x, double y){
        double resultado = x + y;
        return resultado;
    }
    public double Subtracao(double x, double y){
        return x - y;
    }
    public double Multiplicacao(double x, double y){
        return x * y;
    }
    public double Divisao(double x, double y){
        return x / y;
    }
}